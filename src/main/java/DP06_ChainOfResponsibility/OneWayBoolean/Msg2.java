package DP06_ChainOfResponsibility.OneWayBoolean;

public class Msg2 {
    private String msg;

    public Msg2() {
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getMsg() {
        return this.msg;
    }

    @Override
    public String toString() {
        return "Msg{" +
                "msg='" + msg + '\'' +
                '}';
    }
}
