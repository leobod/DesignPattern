package DP13_Builder;

public class MyGoods {
    private String A;
    private String B;

    public String getA() {
        return A;
    }

    public void setA(String a) {
        A = a;
    }

    public String getB() {
        return B;
    }

    public void setB(String b) {
        B = b;
    }

    @Override
    public String toString() {
        return "MyGoods{" +
                "A='" + A + '\'' +
                ", B='" + B + '\'' +
                '}';
    }
}
